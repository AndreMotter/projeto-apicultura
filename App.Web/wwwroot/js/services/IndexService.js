
async function IndexLogar(obj) {
    return new Promise((resolve, reject) => {
        Post('Index/Logar', obj).then(function (response) {
            if (response.status === 'success') {
                resolve(response.data);
            } else {
                reject(response.message);
            }
        }, function (err) {
            console.error(err);
            reject('Erro desconhecido');
        });
    });
}

